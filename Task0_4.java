public class Task0_4 {
    
       static void evenOrOdd(int num){
            
            if(num % 2 == 0){
                System.out.println("Even");
            }else{
                System.out.println("Odd");
            }
        
    }
    
    public static void main(String args[]) {
        
        evenOrOdd(7);

    }
}