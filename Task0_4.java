public class Task0_4 {
    
       static void evenOrOdd(int num){
            
            if(num % 2 == 0){
                System.out.println("even");
            }else{
                System.out.println("odd");
            }
        
    }
    
    public static void main(String args[]) {
        
        evenOrOdd(3);

    }
}
